public class IfElse {
	public static void main(String[] args) {
		int age = 31;
		if(age>60)
			System.out.println("You are old!");
		else if(age>=50)
			System.out.println("You are in your 50's");
		else if(age>=40)
			System.out.println("You are in your 40's");
		else
			System.out.println("You are young!");
	}
}
