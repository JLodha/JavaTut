public class MathClassMethods {
	public static void main (String [] args) {
		//abs
		System.out.println(Math.abs(-27.3));
		System.out.println(Math.abs(-27.3));
		//ceil
		System.out.println(Math.ceil(7.3));
		//floor
		System.out.println(Math.floor(7.6));
		//max
		System.out.println(Math.max(3.4,5.6));
		//min
		System.out.println(Math.min(3.4,5.6));
		//pow
		System.out.println(Math.pow(3,4));
		//squareroot
		System.out.println(Math.sqrt(36));
	}
}
